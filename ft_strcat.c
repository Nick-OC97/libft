#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i;
	int		n;

	i = ft_strlen(s1);
	n = 0;
	while (s2[n] != '\0')
	{
		s1[i] = s2[n];
		i++;
		n++;
	}
	s1[i] = '\0';
	return (s1);
}

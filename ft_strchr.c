#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*p;
	int		i;

	i = 0;
	p = (char*)s;
	while (p[i] != '\0' && s)
	{
		if (p[i] == c)
			return ((char *)s + i);
		i++;
	}
	if (p[i] == '\0' && c == '\0')
		return ((char *)s + i);
	return (NULL);
}
